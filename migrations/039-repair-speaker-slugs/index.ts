import { at, defineMigration, patch, set } from 'sanity/migrate'
import {
  generateSpeakerSlug,
  isCanonicalSpeakerSlug,
  resolveSpeakerSlugs,
  type SpeakerSlugInput,
} from '@/lib/speaker/slug'

/**
 * IMPORTANT: Before running this migration:
 * 1. Always create a backup of your dataset:
 *    npx sanity@latest dataset export production my-backup-filename.tar.gz
 *    (the run-migration.yml workflow does this automatically.)
 *
 * 2. Validate your documents against schema changes:
 *    npx sanity@latest documents validate -y
 */

/**
 * Repair every speaker slug so it is URL-safe, correctly shaped, and unique.
 *
 * WHY: slugs were historically generated by several ad-hoc code paths, so the
 * dataset can contain slugs that are missing, empty/whitespace, non-canonical
 * (uppercase, doubled/leading/trailing dashes, non-`[a-z0-9-]`), stored as a
 * raw string instead of the `{_type:'slug'}` object (see migration 012), or
 * accidentally duplicated across speakers. Phase 1 unified NEW writes on the
 * shared `generateSpeakerSlug`/`generateUniqueSlug` helpers; this migration
 * backfills the EXISTING dataset to the same guarantees.
 *
 * APPROACH (two-phase, because slug uniqueness is a dataset-wide property that
 * a per-document stream cannot see on its own):
 *  - Phase 1 (pre-pass): query ALL published speakers up front with their raw
 *    slug, name, `_createdAt`, and inbound reference count. `resolveSpeakerSlugs`
 *    (a pure, unit-tested helper) computes the final slug for every speaker in
 *    one deterministic pass, resolving collisions.
 *  - Phase 2 (stream): for each streamed speaker, patch only when its stored
 *    slug differs from the planned slug OR is stored in the wrong (string)
 *    shape. A slug that is already canonical, unique, and correctly shaped is
 *    left completely untouched, preserving its public profile URL / SEO.
 *
 * COLLISION RULE: when two speakers would share a slug, the most established one
 * keeps it — most inbound references first, then oldest `_createdAt`, then
 * lowest id as a stable tiebreak — and the others are re-slugged with the
 * smallest free numeric suffix (`-2`, `-3`, …). Nameless / emoji-only /
 * non-Latin names fall back to the shared `"speaker"` base (then suffixed).
 *
 * Idempotent: a second run finds every slug already canonical, shaped, and
 * unique, so it produces no patches. Drafts are skipped; the published document
 * owns the slug and a draft inherits it on publish.
 */

interface SpeakerSlugDoc {
  _id: string
  name?: string
  slug?: string | { _type?: string; current?: string } | null
  _createdAt?: string
  referenceCount?: number
}

/** Read the current slug string regardless of legacy (string) or object shape. */
function readCurrentSlug(slug: SpeakerSlugDoc['slug']): {
  current: string | undefined
  isObject: boolean
} {
  if (typeof slug === 'string') {
    return { current: slug, isObject: false }
  }
  if (slug && typeof slug === 'object') {
    return { current: slug.current ?? undefined, isObject: true }
  }
  return { current: undefined, isObject: false }
}

const isDraft = (id: string): boolean => id.startsWith('drafts.')

export default defineMigration({
  title: 'Repair speaker slugs (canonicalize, reshape, de-duplicate)',
  description:
    'Backfills valid, correctly-shaped, dataset-unique slugs for every ' +
    'speaker using the shared slug helpers. Missing/empty/non-canonical/' +
    'string-shaped/colliding slugs are repaired; valid unique slugs are left ' +
    'untouched to preserve profile URLs. Collisions keep the slug on the most ' +
    'established speaker (most references, then oldest) and re-slug the rest.',
  documentTypes: ['speaker'],

  async *migrate(documents, context) {
    // Phase 1 — pre-pass: build the dataset-wide slug plan from every published
    // speaker so cross-document collisions are resolved deterministically.
    const speakers = await context.client.fetch<SpeakerSlugDoc[]>(
      `*[_type == "speaker" && !(_id in path("drafts.**"))]{
        _id,
        name,
        slug,
        _createdAt,
        "referenceCount": count(*[references(^._id)])
      }`,
    )

    const inputs: SpeakerSlugInput[] = speakers.map((speaker) => ({
      id: speaker._id,
      name: speaker.name,
      currentSlug: readCurrentSlug(speaker.slug).current,
      referenceCount: speaker.referenceCount ?? 0,
      createdAt: speaker._createdAt,
    }))

    const planBySlug = new Map(
      resolveSpeakerSlugs(inputs).map((assignment) => [
        assignment.id,
        assignment.slug,
      ]),
    )

    // Phase 2 — stream: apply the plan, patching only what needs to change.
    for await (const doc of documents()) {
      const speaker = doc as unknown as SpeakerSlugDoc

      // Drafts inherit the published doc's slug on publish; never touch them.
      if (isDraft(speaker._id)) continue

      const finalSlug = planBySlug.get(speaker._id)
      if (!finalSlug) {
        // Not in the pre-pass (e.g. created after it ran). Repair defensively
        // from the name without dataset-wide uniqueness knowledge.
        const { current } = readCurrentSlug(speaker.slug)
        if (isCanonicalSpeakerSlug(current)) continue
        console.warn(
          `  ⚠ Speaker ${speaker._id} was not in the pre-pass; repairing slug from name in isolation`,
        )
        const fallback = generateSpeakerSlug(speaker.name ?? '')
        yield patch(speaker._id, [
          at('slug', set({ _type: 'slug', current: fallback })),
        ])
        continue
      }

      const { current, isObject } = readCurrentSlug(speaker.slug)

      // Skip only when the slug is already the planned value AND stored in the
      // correct object shape. This preserves valid, unique slugs verbatim while
      // still reshaping legacy string slugs (mirrors migration 012).
      if (isObject && current === finalSlug) {
        continue
      }

      if (current === finalSlug) {
        console.log(
          `  ↻ reshape ${speaker._id} (${speaker.name ?? '—'}): "${finalSlug}" (string → slug object)`,
        )
      } else {
        console.log(
          `  ✓ ${speaker._id} (${speaker.name ?? '—'}): "${current ?? '∅'}" → "${finalSlug}"`,
        )
      }

      yield patch(speaker._id, [
        at('slug', set({ _type: 'slug', current: finalSlug })),
      ])
    }
  },
})
